                        <div class="row quest">
                            <div class="col-md-10 quest-title">
                                Qual o melhor tipo de transporte a escolher?
                            </div>
                            <div class="col-md-2">   
                                <span class="plus"><i class="fas fa-plus"></i></span>
                            </div>
                            <div class="col-md-12 answer">   
                                Esta resposta depende de inúmeros fatores que devem ser considerados na carga:
                                </br>
                                - Preço – quanto mais cara a mercadoria, menor é o custo do frete, podendo o frete aéreo ser
                                viável
                                </br>
                                - Peso – cargas extremamente pesadas tendem a ser transportadas via rodoviária, cabotagem
                                e ferroviária.
                                </br>
                                - Volume – Cargas volumosas se encaixam mais em fretes rodoviários, marítimos e ferroviários.
                                </br>
                                - Perecibilidade – cargas perecíveis exigem transporte refrigerado.
                                </br>
                                - Urgência – Se há a necessidade urgente da chegada da carga e ela se encaixa no frete aéreo,
                                esta é a melhor opção, devido à pressa de sua chegada.
                                </br>
                                Diante destas variáveis, podemos dizer o seguinte:
                                </br>
                                Transporte Rodoviário: é o mais utilizado, principalmente para médias e curtas distâncias.
                                </br>
                                Transporte Aeroviário: utilizado principalmente para cargas de valor agregado, que tenham
                                baixo peso, baixo volume e urgência na sua entrega, em pontos distantes.
                                </br>
                                Cabotagem: Muito utilizado para comodities, cargas com muito volume e peso.
                                </br>
                                Em suma, o contratante precisa combinar uma série de variáveis e critérios como: custo,
                                capacidade, tempo, disponibilidade, transit time, sinistro, integridade e distância, entre outros,
                                para definir a melhor opção a escolher.
