                                a carga chega ao destino final, pode ser cobrado do cliente final, diferença de icms sobre o
                                valor do frete e sobre o valor da NF. Em algumas situações, quando a mercadoria é destinada à
                                Pessoa Física, o estado não cobra. Mas são políticas de cada estado e pode depender até do
                                fiscal que irá analisar tal entrada.
                                </br>
                                Existem casos em que o remetente tem que pagar diferença de imposto, nos casos de
                                substituição tributária de ICMS. Nestes casos, o melhor a se fazer é buscar orientação do
                                contador, pois não é uma política única no Brasil e diverge em valores, taxas e definições em
                                cada estado da federação.
