                            <div class="col-md-12 answer">   
                                <b>Frete CIF:</b> Cost, Insuranse and Freight, que querem dizer custo, seguro e frete. Em
                                outras palavras, a responsabilidade do embarcador vai até a entrega das mercadorias
                                em seu destino. 
                                </br>
                                <b>Frete FOB:</b> Free On Board. Na tradução literal quer dizer “livre a bordo”. Neste caso, o
                                destinatário se responsabiliza pela carga, a partir da saída das dependências do
                                remetente (embarcador).
                            </div>
